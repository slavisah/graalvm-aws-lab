package com.comsysto.lab.grlaws;

public class Input {

    private String name;
    private String greeting;

    public String getName() {
        return name;
    }

    public Input setName(String name) {
        this.name = name;
        return this;
    }

    public String getGreeting() {
        return greeting;
    }

    public Input setGreeting(String greeting) {
        this.greeting = greeting;
        return this;
    }
}
